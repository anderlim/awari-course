// AND
const condicaoA = true;
const condicaoB = false;

if (condicaoA && condicaoB) {
    console.log(`AND ${condicaoA} && ${condicaoB} = Condição atingida`);
} else {
    console.log(`AND ${condicaoA} && ${condicaoB} = Condição não atingida`);
}

// OR
const condicaoC = true;
const condicaoD = false;

if (condicaoC || condicaoD) {
    console.log(`OR ${condicaoC} || ${condicaoD} = Condição atingida`);
} else {
    console.log(`OR ${condicaoC} || ${condicaoD} = Condição não atingida`);
}